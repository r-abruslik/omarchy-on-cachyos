#!/bin/bash
set -e
set -u

die() {
    echo "✗ FATAL: $*" >&2
    exit 1
}

run() {
    echo " -> $*"
    "$@" || die "Command failed: $*"
}

echo ">> Ensuring bash is the login shell..."
if [ "$SHELL" != "/usr/bin/bash" ]; then
    echo " → Changing shell to bash for $USER..."
    if chsh -s /usr/bin/bash; then
        echo " ✓ Shell changed (will take effect on next login)"
    else
        echo " ℹ Shell was already bash or change failed"
    fi
else
    echo " ✓ Already using bash"
fi
echo ""

echo ">> Detecting GPU..."
if lspci | grep -i nvidia > /dev/null 2>&1; then
    NVIDIA_GPU_INFO=$(lspci | grep -i nvidia | head -1)
    echo " ✓ NVIDIA GPU detected: $NVIDIA_GPU_INFO"
    
    if pacman -Q nvidia-open-dkms &> /dev/null || pacman -Q nvidia-utils &> /dev/null; then
        echo ""
        echo "CachyOS is currently using NVIDIA open-source drivers (or generic utils)."
        echo ""
        echo "Do you want to switch to NVIDIA proprietary drivers (580.x series)?"
        echo ""
        echo -n "Switch to proprietary drivers? (y/N): "
        read -r SWITCH_TO_PROPRIETARY
        
        if [[ "$SWITCH_TO_PROPRIETARY" =~ ^[Yy]$ ]]; then
            echo ""
            echo ">> Switching to NVIDIA proprietary drivers..."
            
            echo " → Removing kernel-specific nvidia-open modules..."
            for kernel in $(pacman -Qqs linux-cachyos | grep -v headers | grep -v zfs | grep -v dbg); do
                sudo pacman -Rns --noconfirm "${kernel}-nvidia-open" 2>/dev/null || true
            done
            
            echo " → Removing nvidia-open-dkms packages..."
            sudo pacman -Rns --noconfirm \
                nvidia-utils \
                egl-wayland \
                nvidia-settings \
                opencl-nvidia \
                lib32-opencl-nvidia \
                lib32-nvidia-utils \
                libva-nvidia-driver \
                2>/dev/null || true
            
            echo " → Installing nvidia-580xx proprietary packages..."
            run sudo pacman -S --needed --noconfirm \
                nvidia-580xx-dkms \
                nvidia-580xx-utils \
                nvidia-580xx-settings \
                opencl-nvidia-580xx \
                lib32-opencl-nvidia-580xx \
                lib32-nvidia-580xx-utils \
                libva-nvidia-driver \
                vulkan-icd-loader \
                lib32-vulkan-icd-loader \
                egl-wayland

            echo " >> checking chassis type..."
            CHASSIS_TYPE=$(cat /sys/devices/virtual/dmi/id/chassis_type 2>/dev/null || echo 0)


            if [[ "$CHASSIS_TYPE" =~ ^(8|9|10|11)$ ]]; then
                echo " → Laptop detected: Installing hybrid graphics support..."
                run sudo pacman -S --needed --noconfirm nvidia-prime switcheroo-control
                sudo systemctl enable switcheroo-control
    
    cat << 'EOF' | sudo tee /etc/profile.d/nvidia-rtd3-workaround.sh > /dev/null
# This file is automatically generated by install-omarchy.sh
if [ -n "$(lspci -d "10de:*:0302")" ]; then
    export __EGL_VENDOR_LIBRARY_FILENAMES=/usr/share/glvnd/egl_vendor.d/50_mesa.json
fi
EOF
    cat << 'EOF' | sudo install -Dm755 /dev/stdin /usr/lib/systemd/user-environment-generators/20-nvidia-rtd3-workaround
#!/usr/bin/env sh
if [ -n "$(lspci -d "10de:*:0302")" ]; then
    echo "__EGL_VENDOR_LIBRARY_FILENAMES=/usr/share/glvnd/egl_vendor.d/50_mesa.json"
fi
EOF

    echo " ✓ Hybrid graphics configured (RTD3 power management enabled)"

else
    echo " → Desktop detected: Enabling NVIDIA VA-API environment variable..."
    echo "export LIBVA_DRIVER_NAME=nvidia" | sudo tee /etc/profile.d/nvidia-vaapi.sh > /dev/null
fi

            echo " → Configuring mkinitcpio..."
            sudo tee /etc/mkinitcpio.conf.d/10-chwd.conf > /dev/null <<'EOF'
            # This file is automatically generated by chwd. PLEASE DO NOT EDIT IT.
            MODULES=(nvidia nvidia_modeset nvidia_uvm nvidia_drm)
EOF
            
            echo " → Rebuilding initramfs and updating bootloader..."
            echo "y" | sudo mkinitcpio -P
            
            echo " ✓ NVIDIA proprietary drivers (580.x) installed"
            echo ""
            echo "⚠ NOTE: You MUST REBOOT after Omarchy installation completes"
            echo "        for the new NVIDIA drivers to take effect."
            echo ""
            echo -n "Press Enter to continue..."
            read -r
        else
            echo " → Keeping existing drivers"
        fi
    else
        echo " ℹ No NVIDIA open-dkms drivers detected (may already be using proprietary)"
    fi
else
    echo " ✓ No NVIDIA GPU detected"
fi

echo ""
echo ">> Cloning Omarchy..."
rm -rf ../omarchy 2>/dev/null || true
run git clone https://github.com/basecamp/omarchy ../omarchy
cd ../omarchy || die "cannot cd into ../omarchy"
echo " ✓ Cloned"

echo ""
echo ">> Checking yay..."
if ! command -v yay &> /dev/null; then
    echo " Installing yay..."
    run sudo pacman -S --needed --noconfirm base-devel
    rm -rf /tmp/yay
    run git clone https://aur.archlinux.org/yay.git /tmp/yay
    cd /tmp/yay || die "cannot cd /tmp/yay"
    run makepkg -si --noconfirm
    rm -rf /tmp/yay
    cd - || die "cannot return to omarchy directory"
fi
echo " ✓ yay ready"

echo ""
echo ">> Setting up Omarchy repository..."
run sudo pacman-key --keyserver hkp://keyserver.ubuntu.com --recv-keys F0134EE680CAC571
run sudo pacman-key --lsign-key F0134EE680CAC571

if ! grep -q "\[omarchy\]" /etc/pacman.conf; then
    printf '\n[omarchy]\nSigLevel = Optional TrustedOnly\nServer = https://pkgs.omarchy.org/$arch\n' | \
    sudo tee -a /etc/pacman.conf >/dev/null || die "failed to update /etc/pacman.conf"
fi

echo " -> Installing omarchy-keyring..."
if ! sudo pacman -Sy --noconfirm --needed omarchy-keyring; then
     echo " ⚠ Failed to install omarchy-keyring; key updates won't be automatic." >&2
else
     echo " ✓ Omarchy keyring installed"
fi

if ! sudo pacman -Syu --noconfirm; then
    echo " ⚠ pacman -Syu failed; continuing, but Omarchy repo may be out of date" >&2
fi
echo " ✓ Repository configured"

echo ""
echo ">> User configuration"
echo "Username:"
read -r OMARCHY_USER_NAME
export OMARCHY_USER_NAME
echo "Email:"
read -r OMARCHY_USER_EMAIL
export OMARCHY_USER_EMAIL

echo ""
echo ">> Configuring SDDM autologin..."
run sudo mkdir -p /etc/sddm.conf.d
sudo tee /etc/sddm.conf.d/autologin.conf > /dev/null <<EOF
[Autologin]
User=$USER
Session=hyprland
EOF
echo " ✓ Configured"

echo ""
echo ">> Applying CachyOS compatibility patches..."
[[ ! -f "install.sh" ]] && die "install.sh not found. Make sure you are inside the omarchy folder!"

if [[ -f "bin/omarchy-update-restart" ]]; then
    sed -i "s# | sed 's/-arch/\\\\.arch/'##" bin/omarchy-update-restart
    sed -i "s#'{print \$2}'#'{print \$2 \" - \" \$1}' | sed 's/-linux//'#" bin/omarchy-update-restart
    sed -i "/linux-cachyos/ ! s/pacman -Q linux/pacman -Q linux-cachyos/" bin/omarchy-update-restart
fi

if [[ -f "install/omarchy-base.packages" ]]; then
    sed -i '/tldr/d' install/omarchy-base.packages || die "failed to remove tldr package"
fi

if [[ -f "install/preflight/all.sh" ]]; then
    sed -i '/run_logged \$OMARCHY_INSTALL\/preflight\/pacman\.sh/d' install/preflight/all.sh || \
    die "failed to patch install/preflight/all.sh"
fi

if [[ -f "install/config/all.sh" ]]; then
    sed -i '/run_logged \$OMARCHY_INSTALL\/config\/hardware\/nvidia\.sh/d' install/config/all.sh || \
    die "failed to patch install/config/all.sh"
fi

if [[ -f "install/login/all.sh" ]]; then
    sed -i \
    -e '/run_logged \$OMARCHY_INSTALL\/login\/plymouth\.sh/d' \
    -e '/run_logged \$OMARCHY_INSTALL\/login\/limine-snapper\.sh/d' \
    -e '/run_logged \$OMARCHY_INSTALL\/login\/alt-bootloaders\.sh/d' \
    install/login/all.sh || die "failed to patch install/login/all.sh"
fi

if [[ -f "install/post-install/all.sh" ]]; then
    sed -i '/run_logged \$OMARCHY_INSTALL\/post-install\/pacman\.sh/d' install/post-install/all.sh || \
    die "failed to patch install/post-install/all.sh"
fi

if [[ -f "config/uwsm/env" ]]; then
    sed -i 's/omarchy-cmd-present mise && eval "\$(mise activate bash)"/if [ "\$SHELL" = "\/bin\/bash" ] \&\& command -v mise \&> \/dev\/null; then\n eval "\$(mise activate bash)"\nelif [ "\$SHELL" = "\/bin\/fish" ] \&\& command -v mise \&> \/dev\/null; then\n mise activate fish | source\nfi/' config/uwsm/env
    echo " ✓ Patched mise activation for fish/bash"
fi

echo " ✓ CachyOS patches applied"

echo ""
echo ">> Copying patched Omarchy to ~/.local/share/omarchy..."
rm -rf ~/.local/share/omarchy
mkdir -p ~/.local/share/omarchy
cp -a . ~/.local/share/omarchy || die "failed to copy omarchy files"
cd ~/.local/share/omarchy || die "cannot cd to ~/.local/share/omarchy"
echo " ✓ Copied"
echo ""
echo "======================================================================"
echo " READY TO INSTALL OMARCHY WITH FISH SHELL"
echo "======================================================================"
echo ""
echo "✓ CachyOS compatibility patches applied"
echo "✓ SDDM autologin configured for $OMARCHY_USER_NAME"
echo "✓ omarchy-fish added to package list"
echo "✓ omarchy-setup-fish injected into finish sequence"
echo "✓ Git repository preserved for future updates"
echo "✓ NVIDIA drivers configured (580.x + Vulkan + Wayland)"
echo ""
echo "IMPORTANT: Keep bash as your login shell."
echo "Fish will launch automatically in your terminal."
echo ""
echo "Press Enter to launch Omarchy installer..."
read -r

[[ ! -x "install.sh" ]] && chmod +x install.sh
echo ""
echo ">> Launching Omarchy installer..."
./install.sh
